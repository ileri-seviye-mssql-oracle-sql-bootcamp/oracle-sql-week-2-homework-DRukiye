CREATE TABLE LIBRARYS
(
    ID_             NUMBER GENERATED BY DEFAULT AS IDENTITY START WITH 1 PRIMARY KEY,
    MEMBERS_ID      NUMBER,
    BOOK_ID        NUMBER,
    GIVEN_DATE      DATE,
    RECEIVED_DATE   DATE,
    
    CONSTRAINT FK_MEMBERS FOREIGN KEY(MEMBERS_ID)
    REFERENCES MEMBERS(ID_) ON DELETE CASCADE,
    
    CONSTRAINT FK_BOOKS FOREIGN KEY(BOOK_ID)
    REFERENCES BOOKS(ID_) ON DELETE CASCADE
);
