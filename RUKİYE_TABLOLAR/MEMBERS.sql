CREATE TABLE MEMBERS
(
    ID_             NUMBER GENERATED BY DEFAULT AS IDENTITY START WITH 1 PRIMARY KEY,
    NAME_           VARCHAR2(30),
    SURNAME         VARCHAR2(30),
    GENDER          CHAR(2),
    TEL_NO          NUMBER,
    ADDRES_ID       NUMBER,
    
    CONSTRAINT FK_ADDRESS FOREIGN KEY (ADDRESS_ID)
    REFERENCES ADDRESS(ID_) ON DELETE CASCADE
);
